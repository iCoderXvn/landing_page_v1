/**
 * Generate a URL-friendly slug from a title
 */
export function generateSlug(title: string): string {
  return removeVietnameseAccents(title)
    .toLowerCase()
    .trim()
    // Replace multiple spaces with single dash
    .replace(/\s+/g, '-')
    // Remove special characters except dashes and alphanumeric
    .replace(/[^a-z0-9-]/g, '')
    // Remove multiple consecutive dashes
    .replace(/-+/g, '-')
    // Remove leading and trailing dashes
    .replace(/^-+|-+$/g, '');
}

/**
 * Generate a unique slug by appending a number if the slug already exists
 */
export function generateUniqueSlug(title: string, existingSlugs: string[]): string {
  let baseSlug = generateSlug(title);
  let slug = baseSlug;
  let counter = 1;

  // If slug is empty, use a default
  if (!slug) {
    slug = 'untitled';
    baseSlug = 'untitled';
  }

  // Keep appending numbers until we find a unique slug
  while (existingSlugs.includes(slug)) {
    slug = `${baseSlug}-${counter}`;
    counter++;
  }

  return slug;
}

/**
 * Convert existing title to slug for migration purposes
 */
export function titleToSlug(title: string): string {
  if (!title || title.trim() === '') {
    return 'untitled';
  }
  
  return generateSlug(title);
}

/**
 * Validate if a string is a valid slug format
 */
export function isValidSlug(slug: string): boolean {
  const slugPattern = /^[a-z0-9]+(?:-[a-z0-9]+)*$/;
  return slugPattern.test(slug) && slug.length > 0 && slug.length <= 100;
}